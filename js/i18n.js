function prefetchPresentation(url) {
  let link = document.querySelector('link[data-prefetch-pres]');
  if (!link) {
    link = document.createElement('link');
    link.rel = 'prefetch';
    link.as = 'document';
    link.setAttribute('data-prefetch-pres', '');
    document.head.appendChild(link);
  }
  if (link.getAttribute('href') !== url) link.href = url;
}

function updatePresentationLinks(lang) {
  const htmlPath = PRESENTATION_HTML[lang] || PRESENTATION_HTML.tr;
  document.querySelectorAll('[data-i18n-pdf]').forEach(el => {
    el.href = htmlPath;
    el.target = '_blank';
    el.rel = 'noopener noreferrer';
    el.removeAttribute('download');
  });
  prefetchPresentation(htmlPath);
}

function t(key, lang) {
  const l = lang || currentLang;
  return TRANSLATIONS[l]?.[key] || TRANSLATIONS.tr[key] || key;
}

let currentLang = localStorage.getItem('gor-lang') || 'tr';

function setLanguage(lang) {
  if (!TRANSLATIONS[lang]) return;
  currentLang = lang;
  localStorage.setItem('gor-lang', lang);
  document.documentElement.lang = lang;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.textContent = t(key, lang);
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    el.placeholder = t(key, lang);
  });

  const flag = document.getElementById('currentFlag');
  const langLabel = document.getElementById('currentLang');
  if (flag) flag.textContent = LANG_FLAGS[lang];
  if (langLabel) langLabel.textContent = lang.toUpperCase();

  document.querySelectorAll('.lang-switcher__menu li').forEach(li => {
    li.classList.toggle('active', li.dataset.lang === lang);
  });

  updatePresentationLinks(lang);

  if (typeof window.onLanguageChange === 'function') {
    window.onLanguageChange(lang);
  }

  document.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang } }));
}
